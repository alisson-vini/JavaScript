A - B // 1 (subtração)
A * B // 6 (multiplicação)
A / B // 1.5 (divisão)
A ** B // 9 (potenciação)
A % B // 1 (resto da divição)


// operadores de atribuição
A = B (A = B)
A += B (A = A + B)
A -= B (A = A - B)
A *= B (A = A * B)
A /= B (A = A / B)
A %= B (A = A % B)
A **= B (A = A ** B)


// Operadores de comparadores

A == B // comparação (converte tipos)
A === B // conparação (não converte tipos)
A != B // diferente de (coneverte tipor)
A !== B // diferente de (não converte tipos)
A > B // maior que
A >= B // maior ou igual que
A < B // menor que
A <= B // menor ou igual que


